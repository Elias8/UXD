/// Defines the selection tool, drawing tools, text tool, art board tool,
/// and extra.
enum DesignTool {
  /// A selection design tool.
  selector,

  /// A rectangle design tool.
  rectangle,

  /// An ellipse design tool.
  ellipse,

  /// A polygon design tool.
  polygon,

  /// A line design tool.
  line,

  /// A pen design tool.
  pen,

  /// A text design tool.
  text,

  /// An art board design tool.
  artboard,

  /// A zoom design tool.
  zoom,
}

/// Defines the libraries and layers panel.
enum DesignPanel {
  /// The library panel.
  libraries,

  /// The layer panel.
  layers
}
