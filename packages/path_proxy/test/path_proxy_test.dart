import 'package:flutter_test/flutter_test.dart';
import 'package:path_proxy/path_proxy.dart';

void main() {
  group('PathProxy', () {
    group('toFlutterString | toSvgString', () {
      test('should return correct flutter path and svg string', () {
        final flutterLogoPath = PathProxy()
          ..moveTo(59.109, 30.8)
          ..cubicTo(59.109, 30.8, 41.882, 48.037, 41.882, 48.037)
          ..cubicTo(41.882, 48.037, 59.109, 65.27, 59.109, 65.27)
          ..cubicTo(59.109, 65.27, 39.424, 65.27, 39.424, 65.27)
          ..cubicTo(39.424, 65.27, 22.192, 48.037, 22.192, 48.037)
          ..cubicTo(22.192, 48.037, 39.424, 30.8, 39.424, 30.8)
          ..cubicTo(39.424, 30.8, 59.109, 30.8, 59.109, 30.8)
          ..moveTo(39.424, 1.275)
          ..cubicTo(39.424, 1.275, 7.43, 33.269, 7.43, 33.269)
          ..cubicTo(7.43, 33.269, 17.275, 43.114, 17.275, 43.114)
          ..cubicTo(17.275, 43.114, 59.109, 1.275, 59.109, 1.275)
          ..cubicTo(59.109, 1.275, 39.424, 1.275, 39.424, 1.275)
          ..cubicTo(39.424, 1.275, 39.424, 1.275, 39.424, 1.275)
          ..close();

        const svgString = 'M 59.109 30.8C 59.109 30.8 41.882 48.037 41.882 '
            '48.037C 41.882 48.037 59.109 65.27 59.109 65.27C 59.109 65.27 '
            '39.424 65.27 39.424 65.27C 39.424 65.27 22.192 48.037 22.192 '
            '48.037C 22.192 48.037 39.424 30.8 39.424 30.8C 39.424 30.8 59.109 '
            '30.8 59.109 30.8M 39.424 1.275C 39.424 1.275 7.43 33.269 7.43 '
            '33.269C 7.43 33.269 17.275 43.114 17.275 43.114C 17.275 43.114 '
            '59.109 1.275 59.109 1.275C 59.109 1.275 39.424 1.275 39.424 1.275C'
            ' 39.424 1.275 39.424 1.275 39.424 1.275Z';

        const cubicFlutterString = '''
Path()
  ..moveTo(59.109, 30.8)
  ..cubicTo(59.109, 30.8, 41.882, 48.037, 41.882, 48.037)
  ..cubicTo(41.882, 48.037, 59.109, 65.27, 59.109, 65.27)
  ..cubicTo(59.109, 65.27, 39.424, 65.27, 39.424, 65.27)
  ..cubicTo(39.424, 65.27, 22.192, 48.037, 22.192, 48.037)
  ..cubicTo(22.192, 48.037, 39.424, 30.8, 39.424, 30.8)
  ..cubicTo(39.424, 30.8, 59.109, 30.8, 59.109, 30.8)
  ..moveTo(39.424, 1.275)
  ..cubicTo(39.424, 1.275, 7.43, 33.269, 7.43, 33.269)
  ..cubicTo(7.43, 33.269, 17.275, 43.114, 17.275, 43.114)
  ..cubicTo(17.275, 43.114, 59.109, 1.275, 59.109, 1.275)
  ..cubicTo(59.109, 1.275, 39.424, 1.275, 39.424, 1.275)
  ..cubicTo(39.424, 1.275, 39.424, 1.275, 39.424, 1.275)
  ..close();''';

        expect(flutterLogoPath.toSvgString(), svgString);
        expect(flutterLogoPath.toFlutterString(), cubicFlutterString);
      });
    });
  });
}
